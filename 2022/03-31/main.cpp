int go(vector<int>& nums, int m, int i){
  if(m == 0 && i == 0) return 0;
  if(m == 0 || i == 0) return INT_MAX;
  if(dp[m][i] != -1) return dp[m][i];

  // printf("M: %d I: %d\n", m, i);
  int v = INT_MAX;
  int acc = 0;
  for(int j=i; j>0; --j){
    acc += nums[j-1];
    v = min(v, max(acc, go(nums, m-1, j-1)));
  }

  dp[m][i] = v;
  return dp[m][i];
}

int splitArray(vector<int>& nums, int m){
  memset(dp, -1, sizeof(dp));
  return go(nums, m, nums.size());
}

